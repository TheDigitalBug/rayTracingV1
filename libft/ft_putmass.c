#include "libft.h"

void	ft_putmass(char **str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		j = 0;
		while (str[i][j] != '\0')
		{
			write(1, &str[i][j], 1);
			j++;
		}
		i++;
		write(1, "\n", 1);
	}
}
